
public class Ticket {

	//ATTRIBUTI
	
	protected String nomeSpettacolo;
	protected double prezzo;
	protected int matricola;
	
	//COSTRUTTORE
	public Ticket(String nomeSpettacolo, double prezzo, int matricola) {
	
		this.nomeSpettacolo = nomeSpettacolo;
		this.prezzo = prezzo;
		this.matricola = matricola;
	}
}
